export const buildRunMessages = (sourceCode: string, stdin?: string) => {
  const stdinBlock = stdin?.trim()
    ? `\nИспользуй этот стандартный ввод вместо значений по умолчанию:\n\`\`\`\n${stdin}\n\`\`\`\n`
    : "";

  return [
    {
      role: "user" as const,
      content: `Важные правила:

Выполнить код — не просто проверить синтаксис.

Если есть мелкие проблемы, такие как неправильные отступы, исправь их и запусти код.

Покажи только вывод программы, ничего больше.

Нет сообщений об ошибках, если только это не критическая ошибка синтаксиса, которая мешает выполнению. Если ошибка есть, скажи номер строки и несколько слов о том, что мне делать, как если бы я был 7-летним ребёнком. Пожалуйста, не будь грубым, я учусь. Не переписывай весь код, просто укажи ошибку и как её исправить. ЗАБУДЬ ПРО ОТСТУПЫ И НЕ ГОВОРИ МНЕ ПРО НИХ, У МЕНЯ НЕПРАВИЛЬНО РАБОТАЕТ БУФЕР И ПРОБЕЛЫ НЕ КОПИРУЮТСЯ.

Без кавычек, без объяснений, без номеров строк.

Для вызовов input() используй: 5 для чисел, "hello" для строк.

Если код работает правильно в реальном интерпретаторе Python, покажи вывод.${stdinBlock}

\`\`\`python
${sourceCode}
\`\`\``,
    },
  ];
};

export const buildCorrectMessages = (sourceCode: string, stdin?: string) => {
  const stdinBlock = stdin?.trim()
    ? `\nЕсли для понимания поведения нужен ввод, ориентируйся на этот stdin:\n\`\`\`\n${stdin}\n\`\`\`\n`
    : "\nЕсли для понимания поведения нужен ввод, используй 5 для чисел и hello для строк.\n";

  return [
    {
      role: "user" as const,
      content: `Исправь этот Python код так, чтобы он запускался и сохранял исходный смысл.

Верни только полный исправленный код.

Добавь только короткие однострочные комментарии через # рядом с изменениями или прямо над изменёнными строками, чтобы было видно, что ты поменял.

Не добавляй markdown fences.
Не добавляй пояснения до или после кода.
Не меняй части кода без необходимости.
${stdinBlock}
Код:
\`\`\`python
${sourceCode}
\`\`\``,
    },
  ];
};
